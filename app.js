function setLang(lang) {
  localStorage.setItem('lang', lang);
  const trans = translations[lang];

  // Update elements by id
  for (const id in trans) {
    const el = document.getElementById(id);
    if (el) {
      el.textContent = trans[id];
    }
  }

  // Update option elements with data-translate-key attribute
  const options = document.querySelectorAll('option[data-translate-key]');
  const selectsToRefresh = new Set();
  options.forEach(option => {
    const key = option.getAttribute('data-translate-key');
    if (trans[key]) {
      option.textContent = trans[key];
      if (option.parentElement && option.parentElement.tagName.toLowerCase() === 'select') {
        selectsToRefresh.add(option.parentElement);
      }
    }
  });

  // Update other elements with data-translate-key attribute (excluding option elements)
  const elements = document.querySelectorAll('[data-translate-key]:not(option)');
  elements.forEach(el => {
    const key = el.getAttribute('data-translate-key');
    if (trans[key]) {
      el.textContent = trans[key];
    }
  });

  // Refresh select elements to update displayed selected option text
  selectsToRefresh.forEach(select => {
    const selectedIndex = select.selectedIndex;
    select.selectedIndex = -1;
    select.selectedIndex = selectedIndex;
  });

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
}

window.addEventListener("DOMContentLoaded", () => {
  const lang = localStorage.getItem("lang") || "ar";
  setLang(lang);
});
